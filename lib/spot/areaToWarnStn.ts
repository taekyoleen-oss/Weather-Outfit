/** 행정구역코드 앞 2자리 → 기상특보 조회용 stnId (시·도 단위 근사) */
export function areaNoToWarnStnId(areaNo: string): string {
  const p = areaNo.slice(0, 2)
  const M: Record<string, string> = {
    '11': '108',
    '26': '159',
    '27': '143',
    '28': '112',
    '29': '156',
    '30': '133',
    '31': '152',
    '36': '239',
    '41': '109',
    '42': '105',
    '43': '131',
    '44': '232',
    '45': '146',
    '46': '165',
    '47': '136',
    '48': '162',
    '50': '184',
  }
  return M[p] ?? '108'
}
